/** This file contains the class Staff and is used to get the staff information
 *
 */

import java.util.*;

public class Staff {
    private String name;
    private String gender;
    private String role;
    private String ID;

    /**
     * Construct a Staff, giving them a name, gender, role and ID.
     *
     */
    public Staff(String name, String gender, String role, String id) {
        this.name = name;
        this.gender = gender;
        this.role = role;
        this.ID = id;
    }


}
